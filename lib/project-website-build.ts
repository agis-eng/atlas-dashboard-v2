async function githubRequest(path: string, init: RequestInit = {}) {
  const res = await fetch(`https://api.github.com${path}`, {
    ...init,
    headers: {
      Authorization: `Bearer ${GITHUB_TOKEN}`,
      Accept: "application/vnd.github+json",
      "User-Agent": "Atlas-Dashboard",
      ...(init.headers || {}),
    },
  });
  const text = await res.text();
  let data: any = null;
  try {
    data = text ? JSON.parse(text) : null;
  } catch {
    data = text;
  }
  if (!res.ok) {
    throw new Error((data && (data.message || data.error)) || `GitHub API error (${res.status})`);
  }
  return data;
}

async function createRepoAndCommitFiles(repoName: string, files: Record<string, string>) {
  const repo = await githubRequest(`/orgs/${GITHUB_OWNER}/repos`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      name: repoName,
      private: true,
      auto_init: false,
      description: `Website generated by Atlas Dashboard for ${repoName}`,
    }),
  });

  for (const [path, content] of Object.entries(files)) {
    await githubRequest(`/repos/${GITHUB_OWNER}/${repoName}/contents/${encodeURIComponent(path).replace(/%2F/g, "/")}`, {
      method: "PUT",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        message: `Initial site build: ${path}`,
        content: Buffer.from(content).toString("base64"),
        branch: "main",
      }),
    });
  }

  return {
    repoUrl: repo.html_url,
    branch: "main",
  };
}

export async function buildWebsiteRepoForProject(projectId: string) {
  const [projectsData, pagesData, buildsData] = await Promise.all([
    loadYaml<{ projects: any[] }>(PROJECTS_PATH, { projects: [] }),
    loadYaml<{ projectPages: any[] }>(PROJECT_PAGES_PATH, { projectPages: [] }),
    loadYaml<{ builds: any[] }>(PROJECT_SITE_BUILDS_PATH, { builds: [] }),
  ]);

  const project = (projectsData.projects || []).find((item) => item.id === projectId);
  if (!project) {
    throw new Error("Project not found");
  }
  if (project.liveUrl || project.previewUrl || project.repoUrl) {
    throw new Error("Project already has a site or repo; use redesign flow instead.");
  }
  if (!GITHUB_TOKEN) {
    throw new Error("GitHub token not configured for site build");
  }

  const latestPage = (pagesData.projectPages || []).find((item) => item.projectId === projectId);
  if (!latestPage) {
    throw new Error("Create a website draft first");
  }
  if (!latestPage.pageCodeDraft) {
    throw new Error("Latest draft does not contain site code");
  }

  const repoName = slugify(project.name || projectId);
  const files = buildSiteFiles(project, latestPage);
  const github = await createRepoAndCommitFiles(repoName, files);

  const buildRecord = {
    id: `site-build-${projectId}-${Date.now()}`,
    projectId,
    pageId: latestPage.id,
    repoName,
    repoUrl: github.repoUrl,
    branch: github.branch,
    status: "repo-created",
    framework: "nextjs",
    createdAt: new Date().toISOString(),
    files: Object.keys(files),
    title: latestPage.name || project.name,
  };

  buildsData.builds = [buildRecord, ...(buildsData.builds || [])];
  await writeFile(PROJECT_SITE_BUILDS_PATH, yaml.dump(buildsData, { lineWidth: -1, noRefs: true, quotingType: '"', forceQuotes: false }), "utf8");

  project.repoUrl = github.repoUrl;
  project.githubBranch = github.branch;
  project.lastUpdate = new Date().toISOString().split("T")[0];
  if (!project.brain) project.brain = {};
  if (!project.brain.notes) project.brain.notes = [];
  project.brain.notes.unshift(`Website repo created from draft ${latestPage.id}: ${github.repoUrl}`);
  await writeFile(PROJECTS_PATH, yaml.dump(projectsData, { lineWidth: -1, noRefs: true, quotingType: '"', forceQuotes: false }), "utf8");

  return {
    buildRecord,
    project,
  };
}
